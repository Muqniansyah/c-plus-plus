#include <iostream>
using namespace std;

int main() {
  // Nama variabel yang bagus
  int minutesPerHour = 60;
  
  // Oke, tapi tidak mudah untuk memahami apa sebenarnya m itu
  int m = 60;
  
  cout << minutesPerHour << "\n";
  cout << m;
  
  return 0;
}

