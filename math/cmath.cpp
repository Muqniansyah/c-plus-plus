#include <iostream>
#include <cmath> // Pustaka C++ <cmath> atau Include the cmath library
using namespace std;

int main() {
//	Fungsi lainnya, seperti sqrt(akar kuadrat), round(membulatkan angka) dan log (logaritma natural), dapat ditemukan di <cmath>file header
  cout << "Akar kuadrat dari 64 adalah: " << sqrt(64) << "\n";
  cout << "Bulatkan 2,6 ke bilangan bulat terdekat: " << round(2.6) << "\n";
  cout << "Logaritma natural dari 2 adalah: " <<log(2) << "\n";
  
  return 0;
}

