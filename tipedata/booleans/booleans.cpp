#include <iostream>
using namespace std;

int main() {
	//	nilai boolean	
  bool isCodingFun = true;
  bool isFishTasty = false;
  cout << isCodingFun << "\n";
  cout << isFishTasty;

	//  ekspresi boolean
  int x = 10;
  int y = 9;
  cout << (x > y);
		// atau lebih mudahnya dengan cara seperti ini
  cout << (10 > 9);  
  		// menggunakan operator sama dengan ( ==) untuk mengevaluasi suatu ekspresi
  int x = 10;
  cout << (x == 10);
  		// atau lebih mudahnya dengan cara seperti ini
  cout << (10 == 15);
  return 0;
}
