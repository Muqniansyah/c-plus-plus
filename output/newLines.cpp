#include <iostream>
using namespace std;

int main() {
  // Untuk menyisipkan baris baru, Anda dapat menggunakan \n karakter.
  cout << "Hello World! \n";
  
  // Anda juga dapat menggunakan << operator dan menempatkan \n karakter setelah teks, seperti ini.
  cout << "I am learning C++" << "\n";
  
  // Dua \n karakter yang berurutan akan menghasilkan baris kosong.
  cout << "thank you!" << "\n\n";
  
  // Cara lain untuk menyisipkan baris baru adalah dengan endl(endline) manipulator.
  cout << "love you." << endl;
  
  // \t untuk membuat tab horizontal
  cout << "Im Muqni. \t";
  
  // \\ untuk menyisipkan karakter garis miring terbalik (\)
  cout << "Im developer \\";
  
  // \" untuk menyisipkan karakter tanda kutip ganda
  cout << "Mereka memanggilku \"Muni\".";
  
  return 0;
}

